    public void actualizarDatos2(){
        Map<String,String> sql = new HashMap<String,String>();
        sql.put("columnas", "*");
        sql.put("tablas", "servicio");
        sql.put("condicion", "id_cliente ='"+idCliente+"'");           
        int cant = Integer.valueOf(String.valueOf(consultas.getSelect(sql,6).get("cant")[0][0]));
        datos2 = consultas.getSelect(sql, 6).get("cont");
        jTable2.setModel(new DefaultTableModel(datos2, nom_servicios));        
        
        double sumaMontos = 0;
        double sumaSaldos = 0;
        
        for(int i = 0; i<cant;i++){
            sumaMontos = Integer.valueOf(""+datos2[i][3]);
        }
        
        for(int i = 0; i<cant;i++){
            sumaSaldos = Integer.valueOf(""+datos2[i][5]);
        }
        
        jLabel1.setText("Total montos:"+sumaMontos);
        jLabel4.setText("Total montos:"+sumaSaldos);
        
    }
    
