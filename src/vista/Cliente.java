    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            Object objectAnticipo = jComboBox2.getSelectedItem();
            double montoAnticipo = Double.valueOf(jt_combroAnticipo.getText());
            

            
            
            Map<String,String> sql = new HashMap<String,String>();
            sql.put("columnas", "saldo");
            sql.put("tablas", "servicio");
            sql.put("condicion", "id = '"+this.idServicio+"'");           
            int s  = Integer.valueOf(""+consultas.getSelect(sql, 1).get("cont")[0][0]);
            
            Map<String,String> sql1 = new HashMap<String,String>();
            Map<String, Object> sql2 = new HashMap<String, Object>();
            
            if(s>0&& s>=montoAnticipo){
                sql1.put("columnas", "saldo = saldo - " +montoAnticipo);
                sql1.put("tabla", "servicio");
                sql1.put("condicion", "id = " + this.idServicio);
                consultas.setUpdate(sql1);
                
                
                sql2.put("tipo_pago", objectAnticipo);
                sql2.put("id_servicio", this.idServicio);
                sql2.put("monto", "" + montoAnticipo);
                sql2.put("fecha", this.fecha);
                consultas.addInsert(sql2, "anticipo");
                actualizarDatos2();        
                jt_combroAnticipo.setText("");
                
                String literal = new NumeroALetra().Convertir(""+montoAnticipo, true);
                Map parametros = new HashMap();
                parametros.put("id", idServicio);
                parametros.put("literal", literal);
                Reportes r = new Reportes();        
                r.imprimir("Recibo.jasper",parametros);
            }
            else{
                JOptionPane.showMessageDialog(this, "Saldo mayor ");
            }
                    
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "DatosIncorrectos " + e.toString());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

